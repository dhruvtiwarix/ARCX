class WalletManager(models.Manager):
    """
    Custom manager that enforces select_for_update on all write operations.
 
    CRITICAL: Never fetch a wallet for balance modification without this.
    Without select_for_update, two concurrent deposits can both read balance=100,
    both add 50, and both write 150. Correct answer is 200. This is a race condition.
 
    PostgreSQL's SELECT FOR UPDATE places a row-level lock.
    The second transaction waits until the first commits or rolls back.
 
    USAGE (always inside transaction.atomic()):
      from django.db import transaction
 
      with transaction.atomic():
          wallet = Wallet.objects.select_for_update().get(user_id=user_id)
          wallet.arcx_balance += Decimal("10.00")
          wallet.save(update_fields=["arcx_balance", "updated_at"])
    """
    def get_for_update(self, **kwargs):
        """Fetch a wallet with a row lock. Must be inside atomic()."""
        return self.select_for_update().get(**kwargs)
 
    def get_active(self, **kwargs):
        """Fetch active (non-deleted, non-frozen) wallet."""
        return self.filter(deleted_at__isnull=True, is_frozen=False).get(**kwargs)
 
 
class Wallet(ArcxBaseModel):
    """
    One wallet per user.
    Stores ARCX token balance and cost basis for P&L calculations.
 
    arcx_balance:   How many ARCX tokens the user holds.
    cost_basis_inr: Total INR the user spent to buy their current holdings.
                    Used to calculate unrealized P&L on the frontend.
    is_frozen:      True when compliance freezes an account.
                    Frozen wallets can VIEW but not transact.
 
    Precision:
      arcx_balance   → 18 decimal places (matches standard token precision)
      cost_basis_inr → 4 decimal places (sufficient for INR)
    """
    user           = models.OneToOneField(
        User, on_delete=models.PROTECT, related_name="wallet"
    )
    arcx_balance   = models.DecimalField(max_digits=28, decimal_places=18, default=0)
    cost_basis_inr = models.DecimalField(max_digits=20, decimal_places=4, default=0)
    is_frozen      = models.BooleanField(default=False, db_index=True)
    transaction_pin = models.CharField(
        max_length=128, null=True, blank=True,
        help_text="Hashed UPI-style PIN for authorizing B2B transfers."
    )
 
    objects = WalletManager()
 
    class Meta:
        db_table = "wallets"
        indexes  = [
            models.Index(fields=["user", "is_frozen", "deleted_at"]),
        ]
 
    def __str__(self):
        return f"Wallet(user={self.user_id}, balance={self.arcx_balance})"
 
 
