class CircuitBreakerLog(ArcxBaseModel):
    """
    Audit log for every circuit breaker event.
    A circuit breaker is a safety halt — like a stock exchange halting trading
    when prices move too fast.
 
    ARCX has 3 tiers (from circuit_breaker.py):
      Tier 1 (Caution):    Market drop > 5%   → Slow down withdrawals
      Tier 2 (Warning):    Market drop > 10%  → Suspend new deposits
      Tier 3 (Emergency):  Market drop > 20%  → Full halt, read-only mode
 
    event_type tells you what action was taken:
      TRIGGERED  → Circuit breaker activated
      RESOLVED   → Market recovered, normal operations resumed
      OVERRIDDEN → Admin manually overrode the breaker (requires 2FA + reason)
 
    resolved_at: NULL = still active | timestamp = when normal ops resumed.
    Knowing when a breaker was ACTIVE helps answer user disputes:
      "Why couldn't I withdraw on 10th May?" → "Circuit Tier 2 was active."
    """
 
    class Tier(models.TextChoices):
        TIER_1 = "tier_1", "Tier 1 — Caution"
        TIER_2 = "tier_2", "Tier 2 — Warning"
        TIER_3 = "tier_3", "Tier 3 — Emergency"
 
    class EventType(models.TextChoices):
        TRIGGERED  = "triggered",  "Triggered"
        RESOLVED   = "resolved",   "Resolved"
        OVERRIDDEN = "overridden", "Admin Override"
 
    tier                  = models.CharField(max_length=10, choices=Tier.choices, db_index=True)
    event_type            = models.CharField(max_length=20, choices=EventType.choices)
    reason                = models.TextField()
    market_drop_pct       = models.DecimalField(max_digits=6, decimal_places=2)
    tvl_withdrawal_pct    = models.DecimalField(
        max_digits=6, decimal_places=2, null=True, blank=True,
        help_text="% of TVL requested for withdrawal in the trigger window"
    )
    resolved_at           = models.DateTimeField(null=True, blank=True, db_index=True)
 
    class Meta:
        db_table = "circuit_breaker_log"
        ordering = ["-created_at"]
        indexes  = [
            models.Index(fields=["tier", "event_type", "created_at"]),
            models.Index(fields=["resolved_at"]),
        ]
 
    def __str__(self):
        status = "ACTIVE" if self.resolved_at is None else "RESOLVED"
        return f"CircuitBreaker({self.tier}, {self.event_type}, {status})"


