class Transaction(ArcxBaseModel):
    """
    Immutable ledger of every ARCX movement.
 
    Design Decisions:
      1. Immutable: Transactions are NEVER updated after creation.
         If a transaction fails, create a new REVERSAL transaction.
         The audit log must be append-only.
 
      2. Idempotency Key: Before creating a transaction, check if this key
         already exists. If yes → return the existing transaction.
         This makes the API safe to retry (network timeouts, client retries).
 
      3. nav_at_tx: Snapshot of 1 ARCX price at the exact moment of transaction.
         Critical for tax calculations. Price changes; this record doesn't.
 
      4. counterparty_wallet: For TRANSFER type transactions, points to the
         receiving wallet. NULL for DEPOSIT and WITHDRAW.
 
    Transaction Types:
      DEPOSIT   → User bought ARCX (INR → ARCX)
      WITHDRAW  → User sold ARCX  (ARCX → INR)
      TRANSFER  → User sent ARCX to another ARCX user (free, instant)
      DIVIDEND  → Overnight yield accrual (system-generated, no INR movement)
    """
 
    class TxType(models.TextChoices):
        DEPOSIT  = "deposit",  "Deposit"
        WITHDRAW = "withdraw", "Withdraw"
        TRANSFER = "transfer", "Transfer"
        DIVIDEND = "dividend", "Dividend"
 
    class Status(models.TextChoices):
        PENDING   = "pending",   "Pending"
        COMPLETED = "completed", "Completed"
        FAILED    = "failed",    "Failed"
        REVERSED  = "reversed",  "Reversed"
 
    wallet              = models.ForeignKey(
        Wallet, on_delete=models.PROTECT, related_name="transactions", db_index=True
    )
    idempotency_key     = models.UUIDField(unique=True, default=uuid.uuid4, db_index=True)
    tx_type             = models.CharField(max_length=20, choices=TxType.choices, db_index=True)
    amount_arcx         = models.DecimalField(max_digits=28, decimal_places=18)
    amount_inr          = models.DecimalField(max_digits=20, decimal_places=4)
    fee_inr             = models.DecimalField(
        max_digits=20, decimal_places=4, default=0,
        help_text="Instant liquidity fee charged on Route A withdrawals (0.1%, max $100 equivalent)"
    )
    nav_at_tx           = models.DecimalField(max_digits=20, decimal_places=4)
    status              = models.CharField(
        max_length=20, choices=Status.choices, default=Status.PENDING, db_index=True
    )
    settlement_date     = models.DateTimeField(
        null=True, blank=True,
        help_text="For Route B (T+2 standard) withdrawals: when INR settles to bank"
    )
    counterparty_wallet = models.ForeignKey(
        Wallet,
        on_delete=models.PROTECT,
        related_name="incoming_transfers",
        null=True,
        blank=True,
    )
 
    class Meta:
        db_table = "transactions"
        indexes  = [
            models.Index(fields=["wallet", "status", "created_at"]),
            models.Index(fields=["tx_type", "created_at"]),
            models.Index(fields=["idempotency_key"]),
        ]
        # Immutability guard: app-level, not DB-level, but documents intent
        ordering = ["-created_at"]
 
    def __str__(self):
        return f"Tx({self.tx_type}, {self.amount_arcx} ARCX, {self.status})"
 
 
