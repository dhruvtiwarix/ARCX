"""
ARCX Database Models — Phase 2
--------------------------------
All tables follow the MNC-grade non-negotiables:
 
  ✓ UUIDs as primary keys (no auto-increment)
  ✓ created_at / updated_at on every table (auto-managed)
  ✓ Soft deletes via deleted_at (hard DELETE is banned in production)
  ✓ select_for_update() on every wallet write (prevents race conditions)
  ✓ Decimal fields — never Float for money (Float loses precision at ~1e15)
  ✓ Idempotency key on Transactions (safe to retry without double-charge)
 
Why UUID over Auto-Increment?
  Auto-increment exposes business intelligence:
    GET /api/v1/users/1042  → you now know they have ~1041 other users.
  UUID reveals nothing. Standard in every fintech API.
 
Why Soft Deletes?
  Hard DELETE is irreversible. In fintech, you must be able to answer:
    "Show me the full state of this account on the day of audit."
  deleted_at preserves the record. Queries filter WHERE deleted_at IS NULL.
 
Why Decimal, Not Float?
  float(0.1) + float(0.2) == 0.30000000000000004  ← actual Python output.
  Django's DecimalField maps to PostgreSQL NUMERIC(precision, scale).
  NUMERIC is exact. No rounding surprises on ₹10,00,000 transactions.
"""
 
import uuid
from django.db import models
 
 
# ─────────────────────────────────────────────────────────────────────────────
# Base Model — inherited by every table in ARCX
# ─────────────────────────────────────────────────────────────────────────────
 
class ArcxBaseModel(models.Model):
    """
    Every ARCX table gets:
      id          → UUID primary key, generated by Python (not DB sequence)
      created_at  → Set once on INSERT, never updated
      updated_at  → Updated on every SAVE automatically
      deleted_at  → NULL = active | timestamp = soft-deleted
 
    USAGE:
      # Fetch only active records (always do this)
      User.objects.filter(deleted_at__isnull=True)
 
      # Soft-delete a record (never use .delete())
      user.deleted_at = timezone.now()
      user.save(update_fields=['deleted_at', 'updated_at'])
    """
    id         = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    created_at = models.DateTimeField(auto_now_add=True, db_index=True)
    updated_at = models.DateTimeField(auto_now=True)
    deleted_at = models.DateTimeField(null=True, blank=True, db_index=True)
 
    class Meta:
        abstract = True
 
 
