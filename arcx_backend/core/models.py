class NAVHistory(ArcxBaseModel):
    """
    The official daily published NAV.
    This is what users see in their "price history" chart.
 
    report_hash: SHA256 of the full NAV report JSON (from nav_report.py).
    Stored here so any row in this table can be independently verified
    against the JSON file. Tamper-proof audit trail.
    """
    snapshot           = models.ForeignKey(
        VaultSnapshot, on_delete=models.PROTECT, related_name="nav_records"
    )
    nav_date           = models.DateField(unique=True, db_index=True)
    nav_usd            = models.DecimalField(max_digits=20, decimal_places=8)
    nav_inr            = models.DecimalField(max_digits=20, decimal_places=4)
    dividend_accrued_inr = models.DecimalField(max_digits=20, decimal_places=8, default=0)
    report_hash        = models.CharField(max_length=64, db_index=True)  # SHA256 = 64 hex chars
 
    class Meta:
        db_table = "nav_history"
        ordering = ["-nav_date"]
        verbose_name        = "NAV History Record"
        verbose_name_plural = "NAV History Records"
 
    def __str__(self):
        return f"NAV({self.nav_date}, ₹{self.nav_inr})"
 
 
