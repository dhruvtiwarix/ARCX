class VaultSnapshot(ArcxBaseModel):
    """
    Daily photograph of the vault state.
    Written once per day by the Celery EOD task. Never updated.
 
    This is the raw data. NAVHistory derives the published NAV from it.
 
    Why store both?
      VaultSnapshot = raw source data (asset prices, allocations)
      NAVHistory    = derived output (NAV per token, published to users)
      If the NAV formula changes, you can recalculate NAVHistory
      from the original VaultSnapshot without re-fetching market data.
    """
    snapshot_date   = models.DateField(unique=True, db_index=True)
    total_value_usd = models.DecimalField(max_digits=20, decimal_places=4)
    stock_value_usd = models.DecimalField(max_digits=20, decimal_places=4)
    bond_value_usd  = models.DecimalField(max_digits=20, decimal_places=4)
    gold_value_usd  = models.DecimalField(max_digits=20, decimal_places=4)
    cash_value_usd  = models.DecimalField(max_digits=20, decimal_places=4)
    arcx_supply     = models.DecimalField(max_digits=28, decimal_places=18)
    spy_twap        = models.DecimalField(max_digits=12, decimal_places=4)
    tlt_twap        = models.DecimalField(max_digits=12, decimal_places=4)
    gld_twap        = models.DecimalField(max_digits=12, decimal_places=4)
    usd_inr_rate    = models.DecimalField(max_digits=10, decimal_places=4)
 
    class Meta:
        db_table = "vault_snapshots"
        ordering = ["-snapshot_date"]
 
    def __str__(self):
        return f"VaultSnapshot({self.snapshot_date}, total=${self.total_value_usd})"
 
 
