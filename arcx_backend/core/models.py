class User(ArcxBaseModel):
    """
    The ARCX user account.
    Note: We do NOT store passwords here. Django's auth system handles that
    separately. This table stores ARCX-specific user data.
 
    KYC Status drives what a user can do:
      pending   → can browse, cannot transact
      approved  → full access
      rejected  → read-only, cannot deposit
      suspended → frozen by compliance
    """
 
    class KycStatus(models.TextChoices):
        PENDING   = "pending",   "Pending"
        APPROVED  = "approved",  "Approved"
        REJECTED  = "rejected",  "Rejected"
        SUSPENDED = "suspended", "Suspended"
 
    email      = models.EmailField(unique=True, db_index=True)
    full_name  = models.CharField(max_length=255)
    phone      = models.CharField(max_length=20, unique=True, null=True, blank=True)
    kyc_status = models.CharField(
        max_length=20,
        choices=KycStatus.choices,
        default=KycStatus.PENDING,
        db_index=True,
    )
    is_active  = models.BooleanField(default=True, db_index=True)
 
    class Meta:
        db_table = "users"
        indexes  = [
            models.Index(fields=["email", "deleted_at"]),
            models.Index(fields=["kyc_status", "is_active"]),
        ]
 
    def __str__(self):
        return f"User({self.email}, kyc={self.kyc_status})"
 
 
